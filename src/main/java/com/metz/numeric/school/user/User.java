package com.metz.numeric.school.user;

public class User {

	private String name;
	private String lastName;
	
	public String getName() {
		return name;
	}

	public String getLastName() {
		return lastName;
	}

	public User(String name, String lastName) {
		super();
		this.name = name;
		this.lastName = lastName;
	}
	
}
